<div>ABOUT DHANBAD</h1>
<p>Dhanbad (Hindi: धनबाद) is a city in the state of Jharkhand, and is also known as the ‘Coal Capital of India’. Dhanbad is the 79th fastest growing city in world. According to the 2001 census, Dhanbad is among the 35 cities of India with a population of more than one million. Among the Rail Divisions of Indian Railway, Dhanbad Rail Division is the second largest in terms of revenue generation.
Dhanbad is famous for coal mining. Tata Steel, BCCL, ECL and IISCO are some of the companies having coal mines in the district. Coal-mining, coal washing, and coke making are the main coal related industries in the city. IISCO (Indian Iron And Steel Company) is now owned by SAIL, while BCCL & Eastern Coalfields Limited are subsidiaries of CIL (Coal India Limited); these two companies are the largest operators of coal mines in Dhanbad, and have open cast mines as well as underground mines, whereas Tata Steel has mostly underground mines. These companies have developed townships for their employees.</p>
